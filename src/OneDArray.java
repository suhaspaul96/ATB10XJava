public class OneDArray {
    public static void main(String[] args) {

        int[] numbers = new int[3];

        numbers[0] = 97;
        numbers[1] = 98;
        numbers[2] = 99;

        System.out.println("number at index: "+ numbers[1]);

        String[] names = new String[3];

        names[0] = "Suhas";
        names[1] = "Dhaval";
        names[2] = "Avanti";

        System.out.println("name at index: "+ names[2]);
    }
}
