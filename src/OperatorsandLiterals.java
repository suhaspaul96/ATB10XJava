public class OperatorsandLiterals {
    public static void main(String[] args) {
        float a = 10f;
        float b = 2.3f;

        float c = a+b;

        System.out.println("sum is: "+ c);

        int k = 10;
        int d  = 4;

        System.out.println(k+d);
        System.out.println(k-d);
        System.out.println(k*d);
        System.out.println(k/d);
        System.out.println(k%d);

    }
}
